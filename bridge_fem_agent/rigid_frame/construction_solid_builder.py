    def write(self, task: RigidFrameInput, design: RigidFrameDesign, workdir: Path) -> Path:
        workdir.mkdir(parents=True, exist_ok=True)
        script_path = workdir / f"{task.project_name}_rigid_frame_construction_solid_build.py"
        script_path.write_text(self.render(task, design), encoding="utf-8")
        LOGGER.info("Wrote V5 construction-solid rigid-frame Abaqus build script: %s", script_path)
        return script_path


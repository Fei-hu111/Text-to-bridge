def abaqus_name(value):
    if sys.version_info[0] < 3:
        try:
            if isinstance(value, unicode):
                return value.encode("mbcs")
        except NameError:
            pass
    return str(value)


def rounded_key(coord):
    return tuple(round(float(item), 6) for item in coord)


def get_node(node_map, nodes, coord):
    key = rounded_key(coord)
    if key not in node_map:
        node_map[key] = len(nodes) + 1
        nodes.append(key)
    return node_map[key]


def add_hex(node_map, nodes, elements, labels, coords):
    element_nodes = tuple(get_node(node_map, nodes, coord) for coord in coords)
    elements.append(element_nodes)
    label = len(elements)
    labels.append(label)
    return label


def axis_points(start, end, max_size, min_divisions=1):
    length = abs(end - start)
    divisions = max(int(min_divisions), int(math.ceil(length / max(max_size, 1.0e-9))))
    return [start + (end - start) * float(index) / float(divisions) for index in range(divisions + 1)]


def ratio_points(divisions):
    return [float(index) / float(divisions) for index in range(divisions + 1)]


def lerp(left, right, ratio):
    return left + (right - left) * ratio


def y_at(y0_left, y1_left, y0_right, y1_right, x_ratio, y_ratio):
    lower = lerp(y0_left, y0_right, x_ratio)
    upper = lerp(y1_left, y1_right, x_ratio)
    return lerp(lower, upper, y_ratio)


def add_box_variable_y(node_map, nodes, elements, labels, x0, x1, y0_left, y1_left, y0_right, y1_right, z0, z1):
    if x1 <= x0 or y1_left <= y0_left or y1_right <= y0_right or z1 <= z0:
        return []
    max_dx = MESH_CONTROLS.get("longitudinal_size_m", 2.0)
    max_dy = MESH_CONTROLS.get("vertical_size_m", 0.75)
    max_dz = MESH_CONTROLS.get("transverse_size_m", 0.90)
    x_points = axis_points(x0, x1, max_dx)
    z_points = axis_points(z0, z1, max_dz)
    average_y_size = 0.5 * ((y1_left - y0_left) + (y1_right - y0_right))
    y_divisions = max(1, int(math.ceil(average_y_size / max(max_dy, 1.0e-9))))
    y_ratios = ratio_points(y_divisions)
    created = []
    for ix in range(len(x_points) - 1):
        xa = x_points[ix]
        xb = x_points[ix + 1]
        ra = (xa - x0) / max(x1 - x0, 1.0e-9)
        rb = (xb - x0) / max(x1 - x0, 1.0e-9)
        for iy in range(len(y_ratios) - 1):
            ya0 = y_at(y0_left, y1_left, y0_right, y1_right, ra, y_ratios[iy])
            ya1 = y_at(y0_left, y1_left, y0_right, y1_right, ra, y_ratios[iy + 1])
            yb0 = y_at(y0_left, y1_left, y0_right, y1_right, rb, y_ratios[iy])
            yb1 = y_at(y0_left, y1_left, y0_right, y1_right, rb, y_ratios[iy + 1])
            for iz in range(len(z_points) - 1):
                za = z_points[iz]
                zb = z_points[iz + 1]
                created.append(
                    add_hex(
                        node_map,
                        nodes,
                        elements,
                        labels,
                        (
                            (xa, ya0, za),
                            (xb, yb0, za),
                            (xb, yb1, za),
                            (xa, ya1, za),
                            (xa, ya0, zb),
                            (xb, yb0, zb),
                            (xb, yb1, zb),
                            (xa, ya1, zb),
                        ),
                    )
                )
    return created


def add_box(node_map, nodes, elements, labels, x0, x1, y0, y1, z0, z1):
    return add_box_variable_y(node_map, nodes, elements, labels, x0, x1, y0, y1, y0, y1, z0, z1)


def station_props(plan, x):
    stations = plan["stations"]
    if x <= stations[0]["x"]:
        return stations[0]
    if x >= stations[-1]["x"]:
        return stations[-1]
    for left, right in zip(stations[:-1], stations[1:]):
        if left["x"] <= x <= right["x"]:
            span = max(right["x"] - left["x"], 1.0e-9)
            ratio = (x - left["x"]) / span
            result = {{}}
            for key in left.keys():
                if key == "x":
                    result[key] = x
                elif isinstance(left[key], (int, float)):
                    result[key] = left[key] + (right[key] - left[key]) * ratio
                else:
                    result[key] = left[key]
            return result
    return stations[-1]


def section_z_points(props):
    width = props["deck_width_m"]
    bottom_width = props["bottom_box_width_m"]
    web_t = props["web_thickness_m"]
    left_inner = (width - bottom_width) / 2.0
    right_inner = (width + bottom_width) / 2.0
    left_outer = max(0.0, left_inner - web_t)
    right_outer = min(width, right_inner + web_t)
    points = [0.0, left_outer, left_inner, right_inner, right_outer, width]
    return sorted(set(round(point, 6) for point in points))


def build_concrete_mesh(plan):
    node_map = {{}}
    nodes = []
    girder_elements = []
    pier_elements = []
    girder_labels = []
    pier_labels = []
    solid_diaphragm_labels = []

    for left, right in zip(plan["stations"][:-1], plan["stations"][1:]):
        x0 = left["x"]
        x1 = right["x"]
        left_props = station_props(plan, x0)
        right_props = station_props(plan, x1)
        pm = station_props(plan, 0.5 * (x0 + x1))
        height_l = left_props["height_m"]
        height_r = right_props["height_m"]
        top_t_l = left_props["top_slab_thickness_m"]
        top_t_r = right_props["top_slab_thickness_m"]
        bottom_t_l = left_props["bottom_slab_thickness_m"]
        bottom_t_r = right_props["bottom_slab_thickness_m"]
        width = pm["deck_width_m"]
        bottom_width = pm["bottom_box_width_m"]
        left_inner = (width - bottom_width) / 2.0
        right_inner = (width + bottom_width) / 2.0
        left_outer = max(0.0, left_inner - pm["web_thickness_m"])
        right_outer = min(width, right_inner + pm["web_thickness_m"])
        z_points = section_z_points(pm)

        for z0, z1 in zip(z_points[:-1], z_points[1:]):
            add_box_variable_y(node_map, nodes, girder_elements, girder_labels, x0, x1, -top_t_l, 0.0, -top_t_r, 0.0, z0, z1)
        for z0, z1 in ((left_outer, left_inner), (right_inner, right_outer)):
            add_box_variable_y(node_map, nodes, girder_elements, girder_labels, x0, x1, -height_l + bottom_t_l, -top_t_l, -height_r + bottom_t_r, -top_t_r, z0, z1)
        for z0, z1 in ((left_outer, left_inner), (left_inner, right_inner), (right_inner, right_outer)):
            add_box_variable_y(node_map, nodes, girder_elements, girder_labels, x0, x1, -height_l, -height_l + bottom_t_l, -height_r, -height_r + bottom_t_r, z0, z1)

        if pm["solid_zone"] != "hollow":
            added = add_box_variable_y(
                node_map,
                nodes,
                girder_elements,
                girder_labels,
                x0,
                x1,
                -height_l + bottom_t_l,
                -top_t_l,
                -height_r + bottom_t_r,
                -top_t_r,
                left_inner,
                right_inner,
            )
            solid_diaphragm_labels.extend(added)

    for pier in plan["piers"]:
        z0 = pier["z_min"]
        z1 = pier["z_max"]
        x0 = pier["x"] - pier["width_m"] / 2.0
        x1 = pier["x"] + pier["width_m"] / 2.0
        left_props = station_props(plan, x0)
        right_props = station_props(plan, x1)
        y_top_left = -left_props["height_m"]
        y_top_right = -right_props["height_m"]
        pier_slices = max(3, int(pier["height_m"] / plan["mesh_size_m"]))
        for index in range(pier_slices):
            upper_ratio = float(index) / float(pier_slices)
            lower_ratio = float(index + 1) / float(pier_slices)
            y_upper_left = y_top_left + (-pier["height_m"] - y_top_left) * upper_ratio
            y_lower_left = y_top_left + (-pier["height_m"] - y_top_left) * lower_ratio
            y_upper_right = y_top_right + (-pier["height_m"] - y_top_right) * upper_ratio
            y_lower_right = y_top_right + (-pier["height_m"] - y_top_right) * lower_ratio
            add_box_variable_y(node_map, nodes, pier_elements, pier_labels, x0, x1, y_lower_left, y_upper_left, y_lower_right, y_upper_right, z0, z1)

    return nodes, girder_elements, pier_elements, girder_labels, pier_labels, solid_diaphragm_labels


def build_tendon_mesh(plan, tendon):
    nodes = []
    elements = []
    count = max(4, int(abs(tendon["end_x_m"] - tendon["start_x_m"]) / max(plan["mesh_size_m"] * 1.5, 1.0)))
    for index in range(count + 1):
        ratio = float(index) / float(count)
        x = tendon["start_x_m"] + (tendon["end_x_m"] - tendon["start_x_m"]) * ratio
        props = station_props(plan, x)
        if tendon["role"] == "pier_top_negative_moment":
            y = -0.50 * props["top_slab_thickness_m"]
        else:
            y = -props["height_m"] + 0.50 * props["bottom_slab_thickness_m"]
        z = plan["deck_width_m"] / 2.0
        nodes.append((x, y, z))
    for index in range(count):
        elements.append((index + 1, index + 2))
    return nodes, elements


